#include "ft_printf.h"

int		ft_putchar(char c)
{
    write(1, &c, 1);
    return (1);
}

int		ft_putstr(char *str)
{
    int n;

    n = 0;
    while (str[n] != '\0')
    {
        write(1, &str[n], 1);
        n++;
    }
    return (n - 1);
}
